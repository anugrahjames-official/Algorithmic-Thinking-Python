#Comparison operators

a,b=15,10
print(a==b)  # Equal to
c,d=15,15
print(c==d)  # Equal to
x,y=15,10
print(x!=y)  # Not equal to
p,q=5,5
print(p!=q)  # Not equal to
r,s=3,4
print(r>s)   # Greater than
print(r<s)   # Less than
m,n=30,30
print(m>=n)  # Greater than or equal to
print(m<=n)  # Less than or equal to